public class Main {
    public static void main(String[] args) {
        
        System.out.println(Math.abs(-55));  //valore assoluto, quindi senza segno

        System.out.println(Math.min(55, 21));   //valore minimo tra i due

        System.out.println(Math.pow(5, 2));     //valore alla potenza esempio: 5^2

        System.out.println(Math.sqrt(25));         //valore della radice quadrata (square root)

        System.out.println(Math.PI);                //per printare il valore del pi grego

    }
}
